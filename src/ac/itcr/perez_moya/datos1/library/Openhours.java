package ac.itcr.perez_moya.datos1.library;

public class Openhours {
    int start, end;

    public Openhours(int start) {
        this.start = start;
    }

    public Openhours() {
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
    
        
}
