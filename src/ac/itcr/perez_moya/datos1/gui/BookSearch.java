    private void refreshBooks() {

        String libraryName = (String) librariesCombo.getSelectedItem();
        if (librariesCombo.getSelectedIndex() == 0) {
            libraryName = null;
        }

        String topicName = (String) themeCombo.getSelectedItem();
        if (themeCombo.getSelectedIndex() == 0) {
            topicName = null;
        }

        Book newBook = new Book();
        newBook.setTopic(topicName);
        String name = booksName.getText();
        newBook.setName(name);

        DefaultTableModel model = ((DefaultTableModel) bookstable.getModel());
        model.setRowCount(0);
        for (Book book : LibraryManager.getInstance().search(newBook)) {
            model.addRow(new Object[]{
                book.getIssn(), book.getName(), book.getTopic(), book.getPrice(), book.getSoldBooks(), book.getAvailableBooks()
            });
        }
    }

